  •	Definition: Ein Begriff wird in einen neuen, ungewohnten Zusammenhang gestellt, um eine besondere Bedeutung oder Vorstellung zu erzeugen.
  •	Wirkung: Erzeugt Bilder im Kopf des Lesers, emotionalisiert den Text.
  •	Beispiel: "Der Himmel weinte" anstelle von "Es regnete".
2. Vergleich
  •	Definition: Verknüpfung zweier Bereiche durch "wie", um Gemeinsamkeiten hervorzuheben.
  •	Wirkung: Verdeutlicht und veranschaulicht eine Vorstellung.
  •	Beispiel: "Stark wie ein Löwe".
3. Personifikation
  •	Definition: Vermenschlichung von unbelebten Dingen oder Ideen.
  •	Wirkung: Macht abstrakte Begriffe greifbar und emotional zugänglich.
  •	Beispiel: "Die Sonne lacht".
4. Alliteration
  •	Definition: Gleicher Anfangsbuchstabe bei aufeinanderfolgenden Wörtern.
  •	Wirkung: Schafft Klangmuster, macht Texte einprägsam.
