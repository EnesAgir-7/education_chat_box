Im Nativismus wird die These aufgestellt, dass alle Sprachen gemeinsamen grammatischen Grundregeln folgen, die angeboren sind (= angeborene Universalgrammatik). Du folgst beim Erlernen einer Sprache, also einem sogenannten Sprachinstinkt. Das wird damit begründet, dass es theoretisch unendlich viele Sätze gibt, die der Mensch bilden könnte. Diese könntest du ohne die Universalgrammatik gar nicht erzeugen. Dein Spracherwerb ist also nicht nur abhängig von der Umwelt, sondern entsteht aus deinen eigenen, angeborenen Impulsen heraus. Dieser Ansatz wird als evolutionsbiologische Erklärung der menschlichen Sprache angesehen. Wichtige Vertreter sind Noam Chomsky und Steven Pinker.

