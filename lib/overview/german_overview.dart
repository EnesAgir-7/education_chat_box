5. Anapher
  •	Definition: Wiederholung eines Wortes oder einer Wortgruppe am Satz- oder Versanfang.
  •	Wirkung: Verstärkt eine Aussage, schafft Rhythmus.
  •	Beispiel: "Ich sehe was, was du nicht siehst. Ich höre was, was du nicht hörst."
