Ontogenese

Forscher der Ontogenese untersuchen den individuellen Spracherwerb. Wie kann man den Spracherwerb eines Menschen vom Säuglingsalter bis hin zur vollständigen Sprachkompetenz erklären? Wie kann jeder Mensch sprechen lernen? Mit diesen Fragen beschäftigen sich die folgenden Spracherwerbstheorien:

Behaviorismus

Vertreter des Behaviorismus stellten die These auf, dass wir Sprache durch Imitation, Bestrafung und Verstärkung erlernen. Dieser Prozess nennt sich „operante Konditionierung“. Kurz gesagt, Vertreter dieser Theorie gehen davon aus, dass Kinder das Sprechen lernen, indem sie für falsche Laute bestraft und für richtige Laute verstärkt werden. Sie wiederholen die verstärkten Laute, um weiterhin belohnt zu werden und prägen sie sich ein. So lernen sie schlussendlich das Sprechen. Das Kind erwirbt dabei nicht die Sprache um der Sprache willen, sondern um das Lob der Umwelt zu erfahren und Kritik zu vermeiden. Ein wichtiger Vertreter dieser Theorie ist B. F. Skinner.

Reaktion: Wenn Kinder spontane Laute von sich geben, erhalten sie von anderen Menschen eine Reaktion. Diese kann positiv oder negativ ausfallen.

Bestrafung: Bei falschen Lauten, z.B. falscher Aussprache von Wörtern, wird das Kind kritisiert. Unerwünschtes Verhalten wird also *„bestraft“. Dadurch wiederholt das Kind die falschen Laute nicht mehr und reduziert das unerwünschte Verhalten.

Verstärkung: Gib das Kind einen richtigen Laut von sich, wird es dafür gelobt. Das nennen wir positive Verstärkung. Diese führt dazu, dass das Kind den richtigen Laut öfter wiederholt. Durch die Wiederholungen prägt sich das Kind die Laute ein, was wiederum zum sprachlichen Lernen führt.

Der Begriff „Behaviorismus“ leitet sich aus dem englischen Wort „behaviour“ (=dt. Verhalten) ab. Der Behaviorismus untersucht also das beobachtbare Verhalten von Menschen und Tieren.

